const PlatformItem = ({
  icon: Icon,
  name,
  bgColor,
  platform,
}: {
  icon: React.FC<React.SVGProps<SVGSVGElement>>;
  name: string;
  bgColor: string;
  platform: "pc" | "playstation" | "xbox" | "nintendo";
}) => {
  const getSkewClasses = () => {
    switch (platform) {
      case "pc":
      case "playstation":
        return { outer: "md:skew-x-[-20deg]", inner: "md:skew-x-[20deg]" };
      case "xbox":
      case "nintendo":
        return {
          outer: "md:skew-x-[20deg] 2xl:skew-x-[-20deg]",
          inner: "md:skew-x-[-20deg] 2xl:skew-x-[20deg]",
        };
      default:
        return { outer: "", inner: "" };
    }
  };

  const { outer, inner } = getSkewClasses();

  return (
    <div
      className={`about__platform md:bg-[#1010105A] p-0 sm:p-[32px] 2xl:p-[60px] ${outer} md:border-[2px] md:border-primary-main`}>
      <div
        className={`flex flex-col items-center justify-center gap-[16px] ${inner}`}>
        <div
          className={`w-[40px] h-[40px] sm:w-[80px] sm:h-[80px] flex justify-center items-center ${
            bgColor === "playstation"
              ? "rounded-[50%] bg-playstation p-[6px]"
              : bgColor === "nintendo"
              ? "rounded-[50%] bg-nintendo p-[8px]"
              : ""
          }`}>
          <Icon />
        </div>
        <Heading variant="h3">{name}</Heading>
      </div>
    </div>
  );
};

